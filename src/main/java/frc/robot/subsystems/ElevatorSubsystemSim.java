public class ElevatorSubsystemSim extends SubsystemBase {
    private final ElevatorMechanismSim m_elevator = new ElevatorMechanismSim();

    public ElevatorSubsystemSim() {

    }

    public void reachGoal(double goal) {
        m_elevator.reachGoal(goal);
      }

    @Override
    public void periodic() {
        m_elevator.simulationPeriodic();
        m_elevator.updateTelemetry();
    }

  /**
   * Set the goal of the elevator
   *
   * @param goal Goal in meters
   * @return {@link edu.wpi.first.wpilibj2.command.Command}
   */
  public Command setGoal(double goal)
  {
    return run(() -> reachGoal(goal));
  }
}
